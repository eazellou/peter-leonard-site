    <div class="blurb" id="biotext" >
      <p id="biobiobio">Hi, my name is Peter and I’m a senior in the <a href="http://www.music.umich.edu/departments/pat/index.php">Performing Arts Technology Department</a> at the University of Michigan-Ann Arbor studying Sound Engineering. Within the Performing Arts Technology Department, I specialize in post-production, namely mixing and mastering. <br><br>

      I'm adept at both analog and digital mixing methods, and though I enjoy mixing for all types of music and in all environments, I specialize in mixing for live sessions or for quick turnarounds.  I'm well versed in much of multimedia--I have experience editing video and conducting multicam live shoots of performances. <br><br>

      Though engineering, mixing, mastering, and playing bass are my main gigs, I'm also an occasional composer, guitar player, saxophonist, and choral singer. Currently, I'm the bassist in the electro-acoustic outfit Caves, an industrial noise-rock band out of Ann Arbor. I encourage you to <a href="https://www.cavesmusic.com">visit our website</a> to learn more. <br><br>

      I grew up in Chevy Chase, Maryland just outside of Washington DC. While becoming more involved with music as a kid, I became fascinated with the notion of trying to reproduce a musical performance in such a way that could capture enough energy to be compelling to a listener--and that’s led me to where I am today!
      </p>
